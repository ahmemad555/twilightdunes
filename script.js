// Add counter animation for prices
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start);
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target;
        }
    }
    
    updateCounter();
}

// Trigger counter animation when prices come into view
const priceObserver = new IntersectionObserver(function(entries) {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const priceElement = entry.target.querySelector('.price');
            if (priceElement) {
                const priceText = priceElement.textContent;
                const priceNumber = parseInt(priceText.replace('$', ''));
                priceElement.textContent = '$0';
                animateCounter(priceElement, priceNumber);
            }
            priceObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

document.addEventListener('DOMContentLoaded', function() {
    const accommodationCards = document.querySelectorAll('.accommodation-card');
    accommodationCards.forEach(card => priceObserver.observe(card));
});

